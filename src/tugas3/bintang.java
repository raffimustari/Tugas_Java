public class bintang {
    public static void main(String[] args) {
                // Output 1: Segitiga bintang
                int n = 5;
                for (int i = 1; i <= n; i++) {
                    for (int j = 1; j <= i; j++) {
                        System.out.print("* ");
                    }
                    System.out.println();
                }
        
                System.out.println();
        
        }
    }

    
